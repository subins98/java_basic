package variable;

public class Var7 {
    static void main(String[] args) {
        int a = 10; //정수
        double b = 10.5; //실수
        boolean c = true; //불리언(boolean) true, false 만 입력 가능 (주로 참과 거짓을 판단하는 곳에서 사용)
        char d = 'A'; //문자 하나 (작은따옴표 사용)
        String e = "Hello Java"; //문자열, 문자열을 다루기 위한 특별한 타입 (유일하게 첫글자 대문자) (큰따옴표 사용)

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
