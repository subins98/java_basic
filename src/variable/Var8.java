package variable;

public class Var8 {
    static void main(String[] args) {
        //정수 - 높을수록 메모리 공간 많이 차지
        byte b = 127; //-128 ~ 127
        short s = 32767; //-32768 ~ 32767
        int i = 2147483647; // 약 -21억 ~ 21억

        long l = 9223372036854775807L; //뒤에 L 붙이기

        //실수
        float f = 10.0f; //뒤에 f 붙이기
        double d = 10.0;
    }
}
